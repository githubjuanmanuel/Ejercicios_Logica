//¿Cuanto es el x porciento de x número?

const saberPorcentaje = (porcentaje, num) => {
    let porcentajeTotal = num * (porcentaje / 100);
    console.log("El "+ porcentaje +"% de " + num + " es: " + porcentajeTotal);
}
saberPorcentaje( 11, 300);

